    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        Departamento o = new Departamento(
                -1, 
                txtNombre.getText());
        boolean rpta = logica.agregarDepartamento(o);
        if(rpta){
            JOptionPane.showMessageDialog(this, "Se registro correctamente :) !!!");
        }else{
            JOptionPane.showMessageDialog(this, "No se pudo registrar :( !!!");
        }
        logica.imprimir(tbLista);
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void tbListaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbListaMouseClicked
        // TODO add your handling code here:
        //variables
        int fila = -1;
        int id_depa = 0;
        String nombre = "";
        
        //entrada
        fila = tbLista.getSelectedRow();
        
        //proceso y salida
        if(fila>=0){
            id_depa =Integer.parseInt(tbLista.getValueAt(fila, 0)+"");
            nombre =tbLista.getValueAt(fila, 1)+"";
        }
        
        txtID.setText(id_depa+"");
        txtNombre.setText(nombre);
        
    }//GEN-LAST:event_tbListaMouseClicked

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        
        int respuesta = JOptionPane.showConfirmDialog(
                this, "Â¿Desea realmente eliminar el registro?","SISTEMAS",JOptionPane.YES_NO_OPTION);
        
        if(respuesta == JOptionPane.YES_OPTION){
            int id_depa = Integer.parseInt(txtID.getText());
            boolean rpta = logica.eliminarDepartamento(id_depa);
            if(rpta){
                JOptionPane.showMessageDialog(this, "Se elimino correctamente :) !!!");
            }else{
                JOptionPane.showMessageDialog(this, "No se pudo eliminar :( !!!");
            }
        }
        logica.imprimir(tbLista);
        
    }//GEN-LAST:event_btnEliminarActionPerformed

